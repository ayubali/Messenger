package com.ayubsarker.messenger.model;

public class Link {

	private String link;
	private String rel;
	/**
	 * @return the link
	 */
	public String getLink() {
		return this.link;
	}
	/**
	 * @param link the link to set
	 */
	public void setLink(String link) {
		this.link = link;
	}
	/**
	 * @return the rel
	 */
	public String getRel() {
		return this.rel;
	}
	/**
	 * @param rel the rel to set
	 */
	public void setRel(String rel) {
		this.rel = rel;
	}
	
	
	

}
